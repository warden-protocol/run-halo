import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

const root = new URL("../../", import.meta.url);
const contractPath = new URL("contracts/src/HaloVault.sol", root);
const deploymentPath = new URL("vault-core/consensus.json", root);
const outputPath = new URL("vault-core/src/consensus.generated.ts", root);

const contract = readFileSync(contractPath, "utf8");
const deployment = JSON.parse(readFileSync(deploymentPath, "utf8"));

const domainMatch = contract.match(/EIP712\(\s*"([^"]+)"\s*,\s*"([^"]+)"\s*\)/);
const reserveMatch = contract.match(/RESERVE_TYPEHASH\s*=\s*keccak256\(\s*"([^"]+)"\s*\)/s);
const receiptMatch = contract.match(/RECEIPT_TYPEHASH\s*=\s*\n?\s*keccak256\(\s*"([^"]+)"\s*\)/s);

if (!domainMatch || !reserveMatch || !receiptMatch) {
  throw new Error("Unable to extract HaloVault EIP-712 consensus strings");
}
if (!Number.isSafeInteger(deployment.chainId) || deployment.chainId <= 0) {
  throw new Error("consensus.json chainId must be a positive safe integer");
}
if (!/^0x[0-9a-fA-F]{40}$/.test(deployment.vaultAddress)) {
  throw new Error("consensus.json vaultAddress must be a 20-byte hex address");
}

function parseType(typeString) {
  const match = typeString.match(/^([A-Za-z_][A-Za-z0-9_]*)\((.*)\)$/);
  if (!match) throw new Error(`Invalid EIP-712 type string: ${typeString}`);
  const fields = match[2].split(",").map((field) => {
    const fieldMatch = field.trim().match(/^(\S+)\s+([A-Za-z_][A-Za-z0-9_]*)$/);
    if (!fieldMatch) throw new Error(`Invalid EIP-712 field: ${field}`);
    return { name: fieldMatch[2], type: fieldMatch[1] };
  });
  return { name: match[1], fields };
}

const reserve = parseType(reserveMatch[1]);
const receipt = parseType(receiptMatch[1]);
const generated = `// Generated by scripts/gen-consensus.mjs. Do not edit by hand.\n` +
  `// EIP-712 values come from contracts/src/HaloVault.sol; the deployed address\n` +
  `// comes from consensus.json because a contract source cannot encode its own address.\n` +
  `export const VAULT_CHAIN_ID = ${deployment.chainId} as const;\n` +
  `export const VAULT_ADDRESS = ${JSON.stringify(deployment.vaultAddress)} as const;\n` +
  `export const EIP712_NAME = ${JSON.stringify(domainMatch[1])} as const;\n` +
  `export const EIP712_VERSION = ${JSON.stringify(domainMatch[2])} as const;\n` +
  `export const RESERVE_TYPE_STRING = ${JSON.stringify(reserveMatch[1])} as const;\n` +
  `export const RECEIPT_TYPE_STRING = ${JSON.stringify(receiptMatch[1])} as const;\n` +
  `export const RESERVE_TYPES = ${JSON.stringify({ [reserve.name]: reserve.fields }, null, 2)};\n` +
  `export const RECEIPT_TYPES = ${JSON.stringify({ [receipt.name]: receipt.fields }, null, 2)};\n`;

if (process.argv.includes("--check")) {
  let committed = "";
  try {
    committed = readFileSync(outputPath, "utf8");
  } catch {
    // Report the same actionable failure as stale output.
  }
  if (committed !== generated) {
    throw new Error(
      `Generated consensus constants are stale. Run: cd vault-core && npm run generate (${fileURLToPath(outputPath)})`
    );
  }
} else {
  writeFileSync(outputPath, generated);
}
